#define USBDP_PCS_REG_MSK(_pos, _B) \
			(USBDP_PCS_BIT_MASK(_B) << (_pos))
#define USBDP_PCS_REG_CLR(_pos, _B) \
			~(USBDP_PCS_REG_MSK((_pos), _B))
#define USBDP_PCS_REG_SET(_val, _pos, _B) \
			(((_val) & USBDP_PCS_BIT_MASK(_B)) << (_pos))
#define USBDP_PCS_REG_GET(_reg, _pos, _B) \
			(((_reg) & (USBDP_PCS_REG_MSK((_pos), _B))) >> (_pos))

